public class Oxima {
    private String arithmosKyloforias;
    private String marka;
    private String montelo;
    private int kyvikaEkastota;
    private String xroma;

    // Getters
    public String getArithmosKyloforias() {
        return arithmosKyloforias;
    }

    public String getMarka() {
        return marka;
    }

    public String getMontelo() {
        return montelo;
    }

    public int getKyvikaEkastota() {
        return kyvikaEkastota;
    }

    public String getXroma() {
        return xroma;
    }

    // Setters
    public void setArithmosKyloforias(String arithmosKyloforias) {
        this.arithmosKyloforias = arithmosKyloforias;
    }

    public void setMarka(String marka) {
        this.marka = marka;
    }

    public void setMontelo(String montelo) {
        this.montelo = montelo;
    }

    public void setKyvikaEkastota(int kyvikaEkastota) {
        this.kyvikaEkastota = kyvikaEkastota;
    }

    public void setXroma(String xroma) {
        this.xroma = xroma;
    }
}
